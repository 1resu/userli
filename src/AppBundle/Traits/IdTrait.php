<?php

namespace AppBundle\Traits;

/**
 * @author louis <louis@systemli.org>
 */
trait IdTrait
{
    /**
     * @var int
     */
    private $id;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }
}
